// Mirrors k-i18n.js `translate()`: returns the Swedish translation, or `null`
// when nothing matched (so callers can fall back to the original string).
function translate(s: string): string | null {
  const key = s.trim();
  if (!key || !/[A-Za-z]/.test(key)) return null;
  if (Object.prototype.hasOwnProperty.call(EXACT, key)) {
    return s.replace(key, EXACT[key]);
  }
  let out = s;
  for (const [search, replacement] of PHRASES) {
    if (typeof search === 'string') {
      if (out.indexOf(search) >= 0) out = out.split(search).join(replacement);
    } else {
      out = out.replace(search, replacement);
    }
  }
  return out !== s ? out : null;
}

/**
 * Translate a raw English mock-data display string to the active language.
 * - `en` → returned unchanged.
 * - `sv` → EXACT match, else ordered PHRASES, then regex rules (k-i18n parity).
 */
export function localize(text: string, lang: Lang): string {
  if (lang === 'en') return text;
  const translated = translate(text);
  return translated != null ? translated : text;
}

/**
 * Hook returning a localize function bound to the active session language.
 * Usage: `const tx = useLocalize(); ...tx(item.title)`.
 */
export function useLocalize(): (text: string) => string {
  const lang = useSession((s) => s.lang);
  return (text: string) => localize(text, lang);
}
